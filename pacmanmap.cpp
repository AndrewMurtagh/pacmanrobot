bool PacmanMap::isPointOccupied(int x, int y) const {
    int i = x/GRID_SIZE;
    int j = y/GRID_SIZE;
    return (occupancy_grid[j*NUM_GRIDS+i] == 1) ? true : false;

}


void PacmanMap::draw(sf::RenderWindow& window) {
	if(show_map_) {
		for(int i=0; i<NUM_GRIDS; i++) {
	        for(int j=0; j<NUM_GRIDS; j++) {
	            RectangleShape rectangle;
	            rectangle.setPosition(j*GRID_SIZE, i*GRID_SIZE);
	            rectangle.setSize(Vector2f(GRID_SIZE, GRID_SIZE));
	          //  rectangle.setOutlineThickness(1);

	            // walls[i] = rectangle;
	            if(occupancy_grid[i*NUM_GRIDS + j]==0) {
	                rectangle.setFillColor(Color::Black);
	             //   rectangle.setOutlineColor(Color::White);

	            } else {
	                rectangle.setFillColor(Color::Blue);
	             //   rectangle.setOutlineColor(Color::White);

	            }
	            window.draw(rectangle);
	        }
	    }
	}

    if(show_grid_) {
        for(int i=0; i<NUM_GRIDS; i++) {
            RectangleShape rectangle;
            rectangle.setPosition(i*GRID_SIZE, 0);
            rectangle.setSize(Vector2f(1, WINDOW_SIZE));
       
                rectangle.setFillColor(Color::White);
            window.draw(rectangle);
        
        }
        for(int i=0; i<NUM_GRIDS; i++) {
            RectangleShape rectangle;
            rectangle.setPosition(0,i*GRID_SIZE);
            rectangle.setSize(Vector2f(WINDOW_SIZE,1));
       
                rectangle.setFillColor(Color::White);
            window.draw(rectangle);
        
        }
    }

    
    window.draw(start_region);


    for(int i=0; i<6; i++) {
        for(int j=0; j<6; j++) {
            RectangleShape rectangle;
            rectangle.setPosition(goal_region.getPosition().x+i*GRID_SIZE, 
            	goal_region.getPosition().y+j*GRID_SIZE);

            rectangle.setSize(Vector2f(GRID_SIZE, GRID_SIZE));
            if(i%2==0 && j%2==0 || i%2!=0 && j%2!=0) {
            	rectangle.setOutlineColor(Color(230,230,230));
            } else {
                rectangle.setFillColor(Color(12,12,12));
            }

            window.draw(rectangle);
        }
    }
    


	window.draw(pose_text_);
	window.draw(sensor_text_);
	window.draw(goal_text_);
	window.draw(ch_text_);

    
   // window.draw(goal_region);
        

}
        

