sf::Vector2f PacmanMap::getGoalPosition() const {
	sf::Vector2f p;
	p.x = goal_region.getPosition().x+0.5f*goal_region.getSize().x;
	p.y = goal_region.getPosition().y+0.5f*goal_region.getSize().y;
	return p;
}

sf::Vector3f PacmanMap::getStartingPose() const {
	sf::Vector3f pose;
	int minx = (int) start_region.getPosition().x;
	int maxx = (int) start_region.getPosition().x + 6*GRID_SIZE;
	int miny = (int) start_region.getPosition().y;
	int maxy = (int) start_region.getPosition().y + 6*GRID_SIZE;
    pose = sf::Vector3f(
            randomInRange(minx, maxx), 
            randomInRange(miny, maxy), 
            randomInRange(0,360));
    return pose;
}


void PacmanMap::buildRegions(challenge_enum ch) {
	if(ch == ONE) {
	    start_region.setPosition(20*GRID_SIZE, 1*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(20*GRID_SIZE, 39*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));


	} else if(ch == TWO) {
		int sx = randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
		int sy = randomInRange(1*GRID_SIZE, 39*GRID_SIZE);

	    start_region.setPosition(sx, sy);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    int gx=randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    int gy=randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    while(gx>sx-6*GRID_SIZE && gx<sx+6*GRID_SIZE
	    	&& gy>sy-6*GRID_SIZE && gy<sy+6*GRID_SIZE) {
	    	gx=randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    	gy=randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    }
	    goal_region.setPosition(gx, gy);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));


	} else if(ch == THREE) {
	    start_region.setPosition(3*GRID_SIZE, 20*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(37*GRID_SIZE, 20*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));


	} else if(ch == FOUR || ch == SIX) {
		int sx = randomInRange(1*GRID_SIZE, 16*GRID_SIZE);
		int sy = randomInRange(1*GRID_SIZE, 39*GRID_SIZE);

	    start_region.setPosition(sx, sy);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    int gx=randomInRange(25*GRID_SIZE, 39*GRID_SIZE);
	    int gy=randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    goal_region.setPosition(gx, gy);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));



	} else if(ch == FIVE) {
	    start_region.setPosition(3*GRID_SIZE, 3*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(37*GRID_SIZE, 3*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));

	} else if(ch == SEVEN) {
	    start_region.setPosition(34*GRID_SIZE, 6*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(6*GRID_SIZE, 34*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));

	} else if(ch == EIGHT) {
		int sx = randomInRange(1*GRID_SIZE, 39*GRID_SIZE);

	    while(sx>14*GRID_SIZE && sx<28*GRID_SIZE) {
	    	sx = randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    }

	    start_region.setPosition(sx, 1*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    int gx=randomInRange(1*GRID_SIZE, 39*GRID_SIZE);
	    int gy=randomInRange(10*GRID_SIZE, 39*GRID_SIZE);
	    goal_region.setPosition(gx, gy);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));



	}  else if(ch == NINE) {
		int sx = randomInRange(1*GRID_SIZE, 16*GRID_SIZE);
		int sy = randomInRange(1*GRID_SIZE, 39*GRID_SIZE);

	    start_region.setPosition(sx, sy);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(33*GRID_SIZE, 20*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));



	}  else if(ch == TEN) {
	    start_region.setPosition(2*GRID_SIZE, 2*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(16*GRID_SIZE, 23*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*6, GRID_SIZE*6));
	}  


	else {
	    start_region.setPosition(1*GRID_SIZE, 1*GRID_SIZE);
	    start_region.setSize(Vector2f(GRID_SIZE*4, GRID_SIZE*4));
	    start_region.setFillColor(start_colour_);

	    goal_region.setPosition(41*GRID_SIZE, 24*GRID_SIZE);
	    goal_region.setSize(Vector2f(GRID_SIZE*4, GRID_SIZE*4));
	//    goal_region.setFillColor(Color(188,124,124));

	}
}


